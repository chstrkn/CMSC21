#include <stdio.h>

int main(void)
{
    // Declare variables i and j for integers.
    int i, j;
    // Declare variables x and y for floating point numbers.
    float x, y;

    // Assign values to the declared variables.
    i = 10;
    j = 20;
    // Append the letter f to floating point numbers for better readability.
    x = 43.2892f;
    y = 5527.0f;

    // Print the variable names and their associated values.
    printf("i = %d, j = %d, x = %f, y = %f\n", i, j, x, y);

    return 0;
}
